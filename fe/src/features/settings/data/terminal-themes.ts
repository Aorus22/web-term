export interface TerminalThemePreset {
  name: string       // CSS class name: wterm.theme-{name}
  label: string      // Display name
  colors: {
    foreground: string
    background: string
    cursor: string
    black: string        // color-0
    red: string          // color-1
    green: string        // color-2
    yellow: string       // color-3
    blue: string         // color-4
    magenta: string      // color-5
    cyan: string         // color-6
    white: string        // color-7
    brightBlack: string  // color-8
    brightRed: string    // color-9
    brightGreen: string  // color-10
    brightYellow: string // color-11
    brightBlue: string   // color-12
    brightMagenta: string// color-13
    brightCyan: string   // color-14
    brightWhite: string  // color-15
  }
}

/**
 * Map UI theme name to the closest matching terminal theme name.
 * Extracts the base name (removing -dark/-light suffix) and looks up
 * the corresponding terminal theme. Falls back to 'default' for dark
 * themes and 'light' for light themes if no match is found.
 *
 * WTerm uses CSS class-based theming (theme-{name}), so only known
 * theme names with existing CSS rules are returned. XTerm reads from
 * the terminalThemes array directly.
 */
export function getTerminalThemeForUITheme(uiThemeName: string): string {
  const baseName = uiThemeName.replace(/-(dark|light)$/, '')
  const isDark = uiThemeName.endsWith('-dark')

  const knownTerminalThemes: Record<string, string> = {
    default: 'default',
    nord: 'nord',
    dracula: 'dracula',
    monokai: 'monokai',
    one: 'one-dark',
    github: 'github-dark',
    'tokyo-night': 'tokyo-night',
    catppuccin: 'catppuccin',
    gruvbox: 'gruvbox-dark',
    solarized: isDark ? 'solarized-dark' : 'solarized-light',
  }

  if (knownTerminalThemes[baseName]) return knownTerminalThemes[baseName]

  // Fallback to known default CSS classes (WTerm uses CSS class-based theming)
  return isDark ? 'default' : 'light'
}

